package Entity;

public class Servico {
	private int ReservaServico;
	private int CodServico;
	private float PrecoServico;
	private String TipoServico;
	
	public int getReservaServico() {
		return ReservaServico;
	}
	public void setReservaServico(int reservaServico) {
		ReservaServico = reservaServico;
	}
	public int getCodServico() {
		return CodServico;
	}
	public void setCodServico(int codServico) {
		CodServico = codServico;
	}
	public float getPrecoServico() {
		return PrecoServico;
	}
	public void setPrecoServico(float precoServico) {
		PrecoServico = precoServico;
	}
	public String getTipoServico() {
		return TipoServico;
	}
	public void setTipoServico(String tipoServico) {
		TipoServico = tipoServico;
	}
	
	
}
