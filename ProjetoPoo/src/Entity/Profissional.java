package Entity;

public class Profissional {
	private String nomeProfissional;
	private int CPFProfissional;
	private int telefoneProfissional;
	private String emailProfissional;
	private String especializacaoProfissional;
	private int codProfissional;
	
	public String getNomeProfissional() {
		return nomeProfissional;
	}
	public void setNomeProfissional(String nomeProfissional) {
		this.nomeProfissional = nomeProfissional;
	}
	public int getCPFProfissional() {
		return CPFProfissional;
	}
	public void setCPFProfissional(int cPFProfissional) {
		CPFProfissional = cPFProfissional;
	}
	public int getTelefoneProfissional() {
		return telefoneProfissional;
	}
	public void setTelefoneProfissional(int telefoneProfissional) {
		this.telefoneProfissional = telefoneProfissional;
	}
	public String getEmailProfissional() {
		return emailProfissional;
	}
	public void setEmailProfissional(String emailProfissional) {
		this.emailProfissional = emailProfissional;
	}
	public String getEspecializacaoProfissional() {
		return especializacaoProfissional;
	}
	public void setEspecializacaoProfissional(String especializacaoProfissional) {
		this.especializacaoProfissional = especializacaoProfissional;
	}
	public int getCodProfissional() {
		return codProfissional;
	}
	public void setCodProfissional(int codProfissional) {
		this.codProfissional = codProfissional;
	}
	
	
}
