package Entity;

public class Fornecedor {
	private int ProdutoFornecedor;
	private int CnpjFornecedor;
	private int CodFornecedor;
	private String NomeFornecedor;
	
	public int getProdutoFornecedor() {
		return ProdutoFornecedor;
	}
	public void setProdutoFornecedor(int produtoFornecedor) {
		ProdutoFornecedor = produtoFornecedor;
	}
	public int getCnpjFornecedor() {
		return CnpjFornecedor;
	}
	public void setCnpjFornecedor(int cnpjFornecedor) {
		CnpjFornecedor = cnpjFornecedor;
	}
	public int getCodFornecedor() {
		return CodFornecedor;
	}
	public void setCodFornecedor(int codFornecedor) {
		CodFornecedor = codFornecedor;
	}
	public String getNomeFornecedor() {
		return NomeFornecedor;
	}
	public void setNomeFornecedor(String nomeFornecedor) {
		NomeFornecedor = nomeFornecedor;
	}
	
}
